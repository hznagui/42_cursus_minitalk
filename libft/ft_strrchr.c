char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*u;

	u = (char *)s;
	i = 0;
	while (s[i])
		i++;
	if (s[i] == (char) c)
		return (u + i);
	while (i--)
	{
		if (s[i] == (char) c)
			return (u + i);
	}
	return (0);
}
